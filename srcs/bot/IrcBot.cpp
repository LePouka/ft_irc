#include "IrcBot.hpp"

/******************************************************************************/
/*   CONSTRUCTORS / DESTRUCTORS                                               */
/******************************************************************************/
IrcBot::IrcBot( void ) : _name( "Feur" ) {}

IrcBot::IrcBot( IrcBot const & src ) : _name( src._name ) {

	( void )src;
}

IrcBot::~IrcBot( void ) {}

/******************************************************************************/
/*   ASSIGNATION OPERATOR                                                     */
/******************************************************************************/
IrcBot&	IrcBot::operator=( IrcBot const & rhs ) {

	if ( this != &rhs ) {

		_name = rhs._name;
	}

	return *this;
}

/******************************************************************************/
/*   ACCESSORS                                                                */
/******************************************************************************/
void	IrcBot::getName( void ) const {

	return _name;
}

/******************************************************************************/
/*   FUNCTIONS                                                                */
/******************************************************************************/

/******************************************************************************/
/*   ASSIGNATIONS OPERATORS                                                   */
/******************************************************************************/
std::ostream&	operator<<( std::ostream& o, IrcBot const & rhs) {

	o << rhs.getName();
	
	return o;
}
